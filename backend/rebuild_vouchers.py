def make_voucher(date, no, vtype, summary, entries, status="posted"):
    """entries: list of (account_code, debit, credit, description)"""
    v = Voucher(
        company_id=company.id,
        date=date,
        voucher_no=no,
        voucher_type=vtype,
        summary=summary,
        creator_id=1,
        status=status,
    )
    db.add(v)
    db.flush()
    for code, debit, credit, desc in entries:
        db.add(VoucherEntry(
            voucher_id=v.id,
            account_code=code,
            debit=debit,
            credit=credit,
            description=desc or "",
        ))
    return v

vno_counter = [0]
