import { ref, computed, watch } from 'vue'

export type Locale = 'zh-CN' | 'en-US'

const defaultLocale = (import.meta as any).env?.VITE_DEFAULT_LOCALE as Locale | undefined
const savedLocale = (localStorage.getItem('locale') || defaultLocale || 'zh-CN') as Locale
const currentLocale = ref<Locale>(savedLocale)

watch(currentLocale, val => {
  localStorage.setItem('locale', val)
})

export function useI18n() {
  const locale = computed({
    get: () => currentLocale.value,
    set: (val: Locale) => {
      currentLocale.value = val
    },
  })

  function t(key: string): string {
    const keys = key.split('.')
    let result: any = messages[locale.value]
    for (const k of keys) {
      result = result?.[k]
    }
    return typeof result === 'string' ? result : key
  }

  return { locale, t }
}

